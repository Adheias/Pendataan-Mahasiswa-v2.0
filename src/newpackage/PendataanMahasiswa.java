    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        //Input hanya angka
        char char_input = evt.getKeyChar();
        if (((char_input < '0') || (char_input > '9')) && (char_input != '\b')) {
            JOptionPane.showMessageDialog(this, "Masukan Hanya Angka!", "Input Dengan Benar!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTextField1KeyTyped

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        //Input hanya huruf
        char c = evt.getKeyChar();
        if (Character.isLetter(c) || Character.isISOControl(c) || Character.isWhitespace(c)) {
            String text = String.valueOf(c);
        } else {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Masukan Hanya Huruf!", "Input Dengan Benar!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTextField2KeyTyped

