    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Button Save
        String filePath = "C:\\Users\\Adhe Ias Ramadhan\\Documents\\NetBeansProjects\\TubesPL\\src\\Data\\Data.txt";
        File file = new File(filePath);
        try {
            try (FileWriter fw = new FileWriter(file); BufferedWriter bw = new BufferedWriter(fw)) {

                for (int i = 0; i < jTable1.getRowCount(); i++) {//rows
                    for (int j = 0; j < jTable1.getColumnCount(); j++) {//columns
                        bw.write(jTable1.getValueAt(i, j).toString() + ",");
                    }
                    bw.newLine();
                }

            }

        } catch (IOException ex) {
            Logger.getLogger(PendataanMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        //Button Load
        String filePath = "C:\\Users\\Adhe Ias Ramadhan\\Documents\\NetBeansProjects\\TubesPL\\src\\Data\\Data.txt";
        File file = new File(filePath);

        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] lines = br.lines().toArray();

            for (Object line : lines) {
                String[] row = line.toString().split(",");
                model.addRow(row);
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(PendataanMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

